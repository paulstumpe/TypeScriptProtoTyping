//         ------------------------------------------------------------------------------
//             3.5  UNIT STATISTIC EXPLANATION
//         ------------------------------------------------------------------------------
//
//             Name  : The name of the characters
//         Class : There are many type of basic / standard class, together with their
//             upgraded class. For details of class type, please see section 5.3
//         Class List
//         Level : Starting from 1, to a maximum of 20. If your class can be upgraded,
//             using the correct upgrade item, you can upgrade once you were on
//         level 10 or higher. But it is better if you wait until level 20
//         before you upgrade. Benefit : Your attribute will be higher, since
//         every level increase gave a possibility of attribute increase.
//             Attribute that can increase during level increase is Max HP,
//             Strength, Skill, Speed, Luck, Defense, Resistance.
//             Experience : Once you fight with enemy, either they attack you or you attack
//         them, you will get experience. Once your experience reach 100, it will
//         reset to zero, and your level will increase by one. The experience you
//         will get follow this rule :
//             A. When you reached level 20, you will no more get experience
//         increase, since you are at the maximum/highest level already.
//             B. If you do not hit them (either because they are out of your weapon
//         range, or because you miss, or because your hit is "no damage"),
//         then you will get 1 experience
//         C. If you hit them, but do not kill the enemy unit, you will get
//         (their level - your level)/2 + 8 (rounded down ?).
//         This means if your level is higher than enemy, you will receive
//         less than 8 EXP. But if your level is lower than the enemy, you
//         will receive more than 8 EXP. The minimum is 1 EXP.
//             For advance/upgraded class, the formula is complicated by the fact
//         that not every upgraded class is treated the same. Once I found
//         out how the upgraded class is considered, I will put it here.
//             D. If you hit them, and kill the enemy unit, you will get
//         (their level - your level)*3 + 43.
//         This means if your level is higher than enemy, you will receive
//         less than 43 exp. But if your level is lower than the enemy, you
//         will receive more than 43 exp. The maximum is 100 EXP.
//             For advance/upgraded class, the formula is complicated by the fact
//         that not every upgraded class is treated the same. Once I found
//         out how the upgraded class is considered, I will put it here.
//             Note :
//         - Level 1 Thief is considered as Level -12 Archer.
//         - I am still trying to get the correct formula for experience, since
//             it looks like the formula given above is not valid for EVERY instance.
//             Hit Point : Your character current Hit Point. As your character receive injury
//         from the battle, their HP will go lower. To increase the HP, you can
//         use Vulnerary in your inventory (through item menu) or if you have
//         healer, you can use their healing staff to cure.
//             Maximum Hit Point : This is the maximum amount of Hit Point. Whenever you
//         heal, you can not exceed this value. Example : Your HP is 20, and your
//         MHP is 29. If you use a Vulnerary, which will recover 10 HP, you HP
//         should be 30. But since 30 is bigger than your MHP, which is 29, your
//         HP will not become 30, but become 29 instead.
//             Strength : This will affect the damage you inflict to your enemy. The higher
//         the number, the more damage you inflict.
//             Skill : This will be used to calculate the %hit. A higher number improves the
//         unit's hit %.
//         Speed : This affects both evasion and attacks.
//             This will affect whether you will hit enemy once or twice, and also
//         whether your enemy will attack once or twice. If the speed difference
//         is 4 or higher, then the higher speed unit will attack twice. If you
//         are using a weapon that have higher weight than your constitution, then
//         your speed for calculating the number of attack will be affected.
//             Example : enemy speed is 12. Your speed is 16. You should be attacking
//         twice, while your enemy attack only once. But since you are using a
//         weapon that have 12 weight, while your constitution is only 10, then
//         your speed will be considered as 14 ( 16 - (12 - 10). This does not
//         mean that if you are using a weapon that have lower weight value
//         compared to your constitution, you will have an increased speed.
//             Luck  : This affects many different things.
//             This will also affect the calculation of the % hit.
//             Defense : Reduces damage from weapons.
//             This will affect how many damage you will receive from enemy attack
//         that is not magic based.
//             Resistance : Reduces damage from magic attacks.
//             This will affect how many damage you will receive from enemy
//         attack that is magic based.
//             Movement : How much you can move in one turn. Please note that some square
//         need more than one movement for a certain unit. If there is special
//         treatment, I will note it in their own chapter walkthrough. For more
//         information on terrain and their movement cost, please see
//         5.5 Terrain List.
//             Constitution : This is your capability of wielding weapon. Try to avoid using
//         weapon that has higher weight compared to your character constitution
//         since it will make your % avoid value lower (see below for more
//             detailed explanation), and may caused you to be attacked more often
//         (twice instead of once) or attack less often (once instead of twice).
//         Aid    : This is a value that specified your Aid capacity. You can Aid
//         a unit that have same or lower constitution value compared to your
//         Aid value. If the unit have higher value in constitution compared
//         to your Aid value, you can not Aid that unit. Please note that
//         during Aid, your skill & speed will be halved (and rounded down),
//         to reflect the fact that it is much harder to fight when you are
//         lugging another unit. The benefit of a unit that was Aided is that
//         they will not sustain any damage.
//             Traveler : This value will only be filled when you are in Aid mode. The
//         value in this field is the name of the unit that you are traveling
//         with.
//         Affinity : This show the symbol of elements you are strong with.
//         Condition : This value will only be filled if you are affected by special
//         condition (like poisoned, silenced, confused). There will be an
//         explanation of the situation and the number of turns. After the turn
//         counter reached zero, the condition will disappear automatically,
//             and you will return to normal condition.
//             Equipped weapon : Self explanatory.
//             Attack  : Amount of damage this weapon causes. Affected by defenses.
//             Derived from the following formula :
//             Strength (from character stat) + Sharpness (from weapon stat)
//         + Support bonus (cumulative)
//         Other way to get the attack value, is to go to possession  item screen
//         (screen no 2, accessed by R button on the character). See the value of
//         the row and column right under the "Equipment" word, that is the
//         attack value.
//         % Hit   : Chance that the attack will hit. Affected by terrain and enemies.
//             From the following formula :
//             Hit (weapon stat) + 2 * Skill + Luck /2 (rounded down) + Support
//         Bonus (cumulative)
//         % Avoid : Ability to doget enemy attacks. Affects enemy's hit %.
//         From the following formula :
//             2 * Speed + Luck - (Weight - Constitution) * 2 + Support Bonus (cum)
//         If your weapon weight does not exceed your constitution, then the
//         value used in the calculation is zero (not minus).
//         Critical : Chance the weapon will strike a critical blow, doing triple damage.
//             From the following formula :
//             Skill / 2 (rounded down)
//         This statistic show how probable for you to do "critical". In
//             critical attack, your attack damage will be tripled. So if your
//             standard damage is 10, a "critical attack" will give 30 damage.
//             Weapon Level : Each character, depending on their type, will have proficiency
//         in a certain type of weapon (for fighter type) or book/staff (for
//             magic type). If you do not have any proficiency in that type of
//         weapon / magic (denoted by "-"), you will not be able to use it. If
//         your proficiency level is lower than the weapon level requirement,
//             then you are also not able to use the weapon. Example : All legendary
//         weapon have level requirement of S, so if your level is lower than S,
//             then you will not be able to use the legendary weapon. Level from
//         lowest to highest : E - D - C - B - A - S. Please note that for basic
//             class, your highest level of skill is A. Only upgraded class can have
//         skill level of S. If you wanted to increase your skill in a certain
//         weapon type, you need to use the weapon during fight with your enemy.
//             There is a progress bar in each weapon, so you will know when you are
//         nearing weapon level increase (when the bar is full to the right).
//         Support : You will have a maximum of 4 friend that can support you, and give
//         you bonuses in attack, % Hit, % avoid and Critical (Deadly). Level of
//         support will increase from C to B to A (highest). I am still not sure
//         how you can increase the support, but usually I paired the couple and
//         have them fighting the same unit (usually the boss unit). Once they
//         can do support (or increase their support level), it will show up in
//         the menu when they are standing side by side or in the "Army List"
//         menu, support chance screen (Not all support chance show up
//         immediately on the support chance screen).
//         Note : it is possible that the support can increase just by having
//         characters standing by each others when you end each turn.
//
