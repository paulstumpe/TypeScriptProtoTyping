//         ------------------------------------------------------------------------------
//             3.2  BATTLE/CHARACTER MENU
//         ------------------------------------------------------------------------------
//
//             During battle, pressing A button when the cursor square is on top one of your
//         unit (in blue color), it enable your unit to move through movement stage. All
//         square in blue show the square you can move into, while the one in brown color
//         show square you can attack based on all weapon in your unit inventory.
//
//             After the movement phase of your unit, you will encounter the Battle Menu.
//             Depending on your situation, the battle menu will vary. From top to bottom,
//
//             ------------------------------------------------------------------------------
//
//                 VISIT         (only appear when you are on open village door)
//         ATTACK        (only appear when you are adjacent to enemy unit)
//         STEAL         (only appear when you are thief, and there is adjacent enemy
//         units that have item you can steal)
//         STAFF         (only appear when you have ability to use staff)
//         ARENA         (only appear when you are in the arena door square)
//         RESCUE        (only appear when you are adjacent to another character whose
//         Constitution is lower than your Aid value)
//         SEIZE         (only appear when you are on throne/gate square, only show up
//         when using Lin/Eliwood/Hector ??)
//         ARMORY        (only appear when you are in Armory square)
//         DROP          (only appear when you have character under Aid)
//         TAKE          (only appear when you are adjacent to a character that have
//         character under Aid, and your Aid value is higher than
//         the Aided character's constitution)
//         GIVE          (only appear when your character are under Aid, and adjacent
//         to another character whose Aid value is higher than
//         the Aided character's constitution)
//         PLAY          (only appear when you are bard, and there is adjacent ally that
//         have moved in this turn / greyed out)
//         DANCE         (only appear when you are dancer, and there is adjacent ally
//         that have moved in this turn / greyed out) Get 10 EXP.
//         ITEM          (only appear when you have item/weapon in your inventory)
//         TRADE         (only appear when you are next to another character)
//         MERCH         (only appear when you are next to Merlinus)
//         WAIT
//         TALK          (only appear when you are adjacent to an unit you can talk to)
//         DOOR          (only appear when you are in front of locked door, and you have
//         item that can open the locked door)
//         CHEST         (only appear when you are in front of locked chest, and you have
//         item that can open the locked chest)
//         VENDOR        (only appear when you are in vendor square)
//         RIDE          (only appear when you are in Ballista square, and have not
//         mounted the Ballista)
//         EXIT          (only appear when you are in Ballista square, and have mounted
//         the Ballista)
//         SUPPORT       (only appear if you have adjacent units you can have support,
//             and their support level have reached certain value)
//         Note : You can only have Talk or support, but not both. If you can
//         do both talk and support, the menu that show up is talk.
//
//
//         ------------------------------------------------------------------------------
//
//             ITEM.
//                 There will be submenu, which change depending on the situation. Possible
//         option are :
//             Use/Equip : Can be used to change the equipped weapon or use item. If the
//         item selected is a weapon, then the menu is equip. If the item
//         selected is an item (key, treasure key, Vulnerary etc.), then the
//         menu is use.
//             Trade     : This option appear if there is another unit directly beside you.
//             Use this option to change the selected item with your neighbour.
//             The trade submenu will the show up.
//             Discard   : This is always the lowest option. Use this to throw away the item.
//
//             Additional menu and their trigger :
//
//
//             Arena
//         If your unit is on the arena square, you will be given this choice. You will
//         then be told the amount of bet, (700-800), and you will need to choose YES
//         (left one) to start the fight or NO (right one) to cancel your participation.
//             If you lose, your character will die, and you will lose the bet money.
//             If you win, your character will live, gain experience, and you will win 2x
//         the bet money. You can use b to cancel during the fight. You will lose the
//         bet if you cancel, but your character will live. Pressing B will only stop
//         the fight in the next round, so you need to keep your eyes on your character
//         health. Should you think that s/he may loose in the next round of fight, press
//         B.
//
//             TALK
//         If your unit is the Lord, this function is mostly used to recruit enemy.
//             Usually, if a unit has a relationship with another unit, this unit will be
//         able to convince other unit to join. For details on how to recruit a certain
//         character, it can be seen in the chapter walktrough.
//             Hint : If enemy unit (other than boss) has a portrait and name and attribute
//         (wind/fire/magic etc), instead of generic name, usually it mean it can be
//         recruited. You just need to find the right person (not always Lin/Eliwood),
//         it can be somebody else) to recruit that character/unit.
//             This can be done by certain character, usually character that is of the same
//     class and has different opposite type (water and fire, for example). If the
//         two characters have "support capability", then sometime this menu will appear
//         when the characters are adjacent (side-by-side).
//             I suspect that purpose of the talk menu in this instance is to give the
//         initial support level (make it easier to get a support relationship) between
//         both characters.
//
//             SUPPORT
//         This menu will show up once relationship between characters have reached a
//         certain level.
//             This can be done by certain unit, usually unit that is of the same class and
//         has different opposite type (water and fire, for example). If the two unit
//         have "support capability", then this menu will appear when the units are
//         side-by-side.
//             There are three type of support level :
//             A (highest), counted as three levels
//         B (medium), counted as two levels
//         C (lowest), counted as one level
//         One character can only have 5 levels of support. So, possible relationship
//         example :
//             Example 1 :
//         Character 1   A level
//         Character 2   B level
//         or
//         Example 2 :
//         Character 1   B level
//         Character 2   C level
//         Character 3   C level
//         Character 4   C level
//         All of above relationship example used up 5 level of support, so this
//         character can no longer have additional support with any other character.
//             Indication that your character already have maximum support is that all
//         support text will be green colored, instead of the standard Blue colored text.
//             Detailed info on this will be explained on 5.7 Support List
//
//         Example :
//             After
//         choosing this menu, there will be the name of the friend in the "weapon and
//         support level", together with the support level (always start from C).
//         If you wanted to know who wanted to support which, choose "Army List" from
//         FIELD MENU, go to page 6, and anyone who can upgrade the support level will
//         have the name of their partner show up on this page.
//             Based on my experience, I will also write down on the character list who
//         character can have relationship with that character.
//
//             STEAL
//         Can only be done by thief unit when they are adjacent to enemy unit, and the
//         enemy unit has inventory that is neither weapon, book, staff.
//             Every time you steal, you will get +10 EXP.
//
//
//             DOOR
//         Can only be done by thief that still has remaining lockpick or other class
//     that has the key (see item list for more information). If thief that has
//         lockpick and key wanted to use the key, he/she will have to use the "use
//         item" menu, since this menu automatically use the lockpick. Key is key that
//         has only one maximum usage.
//
//             CHEST
//         Can only be done by thief that still has remaining lockpick or other class
//     that has the treasure key (see item list for more information). If thief
//         that has lockpick and treasure key wanted to use the treasure key, he/she
//         will have to use the "use item" menu. Treasure key is key that have five
//         maximum usage. Once the chest have been opened, then "chest" become "empty
//         chest".
//
//         VISIT
//         This will happen when you are in the square that denote open fence village.
//             Using this option will trigger an conversation with the village resident,
//             and usually you will get money or item. This option will also appear
//         when you are in the house (house is single square, while village is usually
//         4 square or 2x2).
//
//         MERCH
//         This option will show up when your character stop in a square directly next
//         to Merlinus. Choosing this will show another set of menu :
//             Give     : choose any of your character's item, and this item will
//         be stored.
//             Take     : to get an item into your character's inventory
//         There are many tabs (sword, spear, axe, bow etc) and you can
//         navigate/change active tab using the d-pad left or right.
//
//             RESCUE, TAKE, GIVE and DROP
//         If your unit (A) is next to another unit (B), and A's Aid value is same
//         or higher than the constitution value of B, then "Rescue" will show up.
//             If you select this, then B will disappear from the map, since it become one
//         with A. As a result of A rescuing B, A will have his "Skill" and "Speed"
//         value halved and rounded down. The "Skill" and "Speed" value will return
//         to the original value once A is no longer in the rescuing state, by dropping
//         B, or having another character transfer B from his safe keeping.
//             If you move a character (A) to square adjacent to the character in Aid
//         state (B, Aided person C), if A's Aid value is same or higher than C's
//         constitution value, then "TAKE" will show up. If you select this, C will
//         be transferred from B to A, and B's penaly in Skill and Speed will dissapear,
//         while A will get the Skill and Speed penalty.
//             If A is in Aid state (have B in safe keeping), and there is another
//         character (C), whose Aid value is same or higher than B's constituion value,
//         "GIVE" will show up. Choosing "GIVE" will make B transferred from A to C,
//             A will lose "Skill" and "Speed" penalty, and C will get the Sill and Speed
//         penalty.
//             If A is in Aid state (have B in safe keeping), and there is an empty
//         adjacent square on A's position, "DROP" will show up. Choosing "DROP"
//         and determining which square you want B to appear on, will result in B
//         showing up on the designated square (B lose the Aid state damage
//         protection), and A will lose "Skill" and "Speed" penalty, since A is no
//         longer in Aid state.
//
//             RIDE and EXIT
//         When you are in Ballista square, the "RIDE" option will show up. It enable you
//         to use the Ballista. If you wanted to stop using the Ballista, use the "EXIT"
//         option, and you will dismount the Ballista.
//
//
//             SEIZE
//         Some chapter will have winning condition : seize throne. After you
//         kill the boss, Lin/Eliwood can go to the throne (usualy a square that were
//         occupied by the boss), and this option will show up. Choosing this option
//         will end current chapter, and start any post chapter conversation.
//
//         ------------------------------------------------------------------------------
//             3.3  FIELD MENU
//         ------------------------------------------------------------------------------
//
//             If you press A in empty square (none of the character, either yours or
//         enemies', is in this square), or you press A on your character that has
//         finished his move in the turn (this unit will turn grey), the Field Menu will
//         show up.
//             There is always five options in this menu, which are :
//             UNIT
//         STATUS
//         OPTIONS
//         SUSPEND
//         END
//
//         Unit : Using this menu, it will show up all of the character that is in
//         the current map. There are six screens, with the following names and
//         columns :
//             1.  Character: Name, Class, Level, Experience, Hit Point, Maximum Hit
//         Point
//         2.  Fighting Skill : Name, Strength, Skill, Speed, Luck, Defense, Magic
//         Resistance
//         3.  Equipment : Name, Equipped Weapon, Attack, % Hit, % Avoid.
//         4.  Personal Data : Name, Movement, Constitution, Aid, Traveler, Condition.
//         5.  Weapon Level : Name, Sword, Spear, Axe, Bow, Magic, Light, Dark, Staff.
//         6.  Support Chance : Name, Ally 1, Ally 2, Ally 3
//         For explanation of the above mentioned attribute, please see 3.5 Unit
//         Statistic Explained.
//
//             Status :
//         In this screen, at the top is the chapter number and name (title).
//             On left part, under title, is the winning requirement. Some of the Winning
//         requirement examples/possibilities :
//         - Occupy the throne
//         - Defeat all enemies in XX turns
//         - Protect for xx turns
//         Under winning requirement is the current turn no, and Gold (G) that you have.
//             On the right, upper part, in blue box, is the no of character you have
//         participating in current battle. In red box, is the no of enemies.
//             Under the red and blue box, is the leader (name, Level and HP). If your
//         cursor in on enemy box (red), the leader shown is enemy's leader (boss). If
//         cursor is on player box (blue), the leader show is your leader.
//             Below that, there is the timer, which keep track of how long you have played
//         this game.
//
//             Options :
//         There are many menu in this screen. It is suggested not to change the
//         configuration except for animation setup. From top to bottom :
//             - Animation :
//         1 (Animate, without background)
//         2 (Animate with background),
//         OFF  (No Animation for all unit)
//         Solo Unique Animation (you can choose setting for each character).
//         Possibilities setting for each character :
//             1 (Animate, without background)
//         2 (Animate, with background)
//             OFF (No animation)
//         - Game Speed     : Normal  Fast
//         - Text Speed     : Slow    Normal  Fast  Max
//         - Terrain Window : On      Off          (when cursor is on any terrain)
//         On means that you will see terrain window (windows in the corner of the
//         battle map, that tell you terrain type name, their defense and avoid
//         characteristic).
//         - Unit Window    : Panel   Burst   Off  (when cursor in on any character)
//         Panel setting show Picture, Name, HP/MHP in bar format
//         Burst setting show Name, HP/MHP in number format
//         Off setting does not show anything.
//         - Combat         : Strat   Detail  Off
//         Strat  setting show basic combat info window
//         Detail setting show detail combat info window
//         - Show Objective : On      Off
//         - Controller     : On      Off
//         - Subtitle Help  : On      Off
//         - Auto Cursor    : On      Off
//         On means the cursor will automatically moved to main Hero (Lin/Eliwood
//         at start of next turn
//         - Music          : On      Off
