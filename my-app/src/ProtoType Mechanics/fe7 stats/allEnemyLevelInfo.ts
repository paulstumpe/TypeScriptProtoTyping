//         Q : I do not understand your FAQ (especially the turn-by-turn FAQ). They
//         said : Move Sain to (4,13). Where is (4,13) ?
//             A : I use (X,Y) coordinate to refer to square in a MAP. X is horisontal,
//         while Y is vertical. (1,1) denote lower-left most square (or South
//         Western most square).
//         Example : In chapter 2 MAP, House is on (1,1), (1,3) and (2,3). Fort is
//         on (3,8) and (4,8).
//
//             Q : How many extra chapters in this game, and what are the requirement to
//         reach those extra chapters ?
//             A : List of extra chapters, and the requirement to reach them (not a
//         complete list yet) :
//         - 7X : Finish chapter 7 in 15 turns or less. Even if you do not go to
//         North East village, as long as you finish this chapter in time,
//             you can go to 7X.
//         - 13X : No specific requirement. One possible requirement is visiting
//         the village.
//         - 16X : There is no # of turn requirement to go to this chapter. I've
//         got feedback that you need to save at least one ally soldiers
//         (that was in prison).
//         - 18X : Finish chapter 18 in 15 turns or less.
//         - 22X : Recruit Hawkeye and possibly you also need to get 700 EXP in
//         chapter 22.
//
//         Q : Where can I get Arena ?
//             A : Arena are available in the following chapters (not a complete list yet) :
//         - 16X
//         - 20
//         - 23B
//
//         Q : Which chapter have secret shop ?
//             A : Secret shop are available in the following chapters :
//             - 19 : 2 Secret shops, in (22,1) and (8,17)
//         - 20 : 1 Secret shop, in (3,21)
//
//         Q : Should I follow your FAQ to the letter, and bring the characters that
//         you brought ? I only use your FAQ when I need it, and I develop different
//         characters.
//             A : Please feel free to make the necessary changes based on your actual
//         situation. Example : In my game, Rebecca have better stats compared to
//         Wil, but if in your case you have developed Wil, then every time I
//         suggest you bring Rebecca, you substitute Rebecca with Wil. General
//             suggestion : choose a different character with the same class, or at
//         least a same type weapon wielder class (Myrmidon and mercenary, both
//         are using sword, so you can substitute Guy with Raven or vice versa).
//
//         Q : I can not access the secret shop. Help !!
//         A : To access the secret shop, you need to put the member card in one
//         characters's inventory, and select the "secret" menu that show up when
//         you are standing in the correct square. If a character doesn't have the
//         member card in their inventory, the "secret" menu will not show up. You
//         can steal the member card from enemy thief that show up in Chapter 19.
//         For more details, you can see Chapter 19 walkthrough.
//
//             Q : Can you bring the people who died back to life and if you can how?
//             A : I'm not 100% sure yet, but I think you can not. Exception is for tutorial
//         chapter (Lyn's chapter, Chapter Prologue - Chapter 10). In these chapter,
//         all who died do not really died, they just disappear and will show up
//         in Eliwood's chapter.
