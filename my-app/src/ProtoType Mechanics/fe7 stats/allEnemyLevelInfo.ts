//             3.6  GENERAL TACTIC
//         ------------------------------------------------------------------------------
//
//             - Weapon will break. Once the usage reached zero, then the weapon break, and
//         you can longer use that weapon. It is important to plan ahead and never run
//         out of weapons.
//         - Try to conserve your good weapon for fight against difficult enemy (high
//         level, high defense). Otherwise, use an iron weapon, which have a high hit %
//         value. An exception to this tactic is the pegasus unit, which have a very
//         low constitution, so you will be better of using slim spear and slim sword.
//             In my opinion, steel weapon is pretty much useless in the game (they have
//         increased weight and decreased accuracy compared to iron), except steel axe.
//         - As much as possible, use a weapon that have a lower weight compared to your
//         constitution. This will make your % avoid maximum, and you will have less
//         chance of sustaining any damage. It will also maximize your speed, since
//         you will not suffer a speed penalty (weight - constitution). Higher speed
//         (less speed penalty) means you may be able to hit the enemy twice, and
//         minimize situation in which your enemy attack you twice.
//         - When you wanted to fight, please consider the weapon you use (especially for
//             paladin class, which can use sword, spear and axe). Use the following chart
//         of weapon and magic strong against and weak against :
//             Sword  : strong vs. Axe, weak vs. Spear
//         Spear  : strong vs. Sword, weak vs. Axe
//         Axe    : strong vs. Spear, weak vs. Sword
//
//         Anima  : strong vs Light, weak vs. Dark
//         Dark   : strong vs Anima, weak vs. Light
//         Light  : strong vs Dark, weak vs. Anima
//         If you are strong, you will have 15% bonus to your damage and %Hit
//         If you are weak, you will suffer 15% additional damage and % Hit.
//             Note : Some weapon will give reverse effect of this strong and weak vs.
//             Look up the weapon detail to see whether the weapon has reverse strong
//         and weak.
//         - Thief and Bandit can destroy village, so kill them as soon as possible.
//         - Note than some weapon are critical against a certain class. Since critical
//         give you twice or more damage (your strength is effectively doubled), use
